def add(a,b): 
    """add 

    Add a and b and return result

    Args:
        a (int): number 1
        b (int): number 2

    Returns:
        int:  sum of a and b 
    """
    return a + b

def minus(a,b):
    """minus 

    Minus a and b

    Args:
        a (int): number 1
        b (int): number 2
    """
    
    return a-b

def multiply(a,b):
    """multiply 

    Multiply numbers together

    Args:
        a (int): number 1
        b (int): number 2
    """
    
    return a * b